class RetryableAPIClient:
    """
    textRetrytextAPIClientEncapsulation
    """
    
    def __init__(
        self,
        max_retries: int = 3,
        initial_delay: float = 1.0,
        max_delay: float = 30.0,
        backoff_factor: float = 2.0
    ):
        self.max_retries = max_retries
        self.initial_delay = initial_delay
        self.max_delay = max_delay
        self.backoff_factor = backoff_factor
    
    def call_with_retry(
        self,
        func: Callable,
        *args,
        exceptions: Tuple[Type[Exception], ...] = (Exception,),
        **kwargs
    ) -> Any:
        """
        ExecuteFunctiontextFailedtextRetry
        
        Args:
            func: textFunction
            *args: FunctionParameters
            exceptions: textRetrytextExceptionType
            **kwargs: FunctiontextParameters
            
        Returns:
            FunctionReturnstext
        """
        last_exception = None
        delay = self.initial_delay
        
        for attempt in range(self.max_retries + 1):
            try:
                return func(*args, **kwargs)
                
            except exceptions as e:
                last_exception = e
                
                if attempt == self.max_retries:
                    logger.error(f"APItext {self.max_retries} textRetrytextFailed: {str(e)}")
                    raise
                
                current_delay = min(delay, self.max_delay)
                current_delay = current_delay * (0.5 + random.random())
                
                logger.warning(
                    f"APItext {attempt + 1} textFailed: {str(e)}, "
                    f"{current_delay:.1f}textRetry..."
                )
                
                time.sleep(current_delay)
                delay *= self.backoff_factor
        
        raise last_exception
    
    def call_batch_with_retry(
        self,
        items: list,
        process_func: Callable,
        exceptions: Tuple[Type[Exception], ...] = (Exception,),
        continue_on_failure: bool = True
    ) -> Tuple[list, list]:
        """
        textFailedtextRetry
        
        Args:
            items: textProcesstextProjectList
            process_func: ProcessFunction，ReceivetextitemtextParameters
            exceptions: textRetrytextExceptionType
            continue_on_failure: textFailedtextContinueProcesstext
            
        Returns:
            (SuccessResultList, FailedtextList)
        """
        results = []
        failures = []
        
        for idx, item in enumerate(items):
            try:
                result = self.call_with_retry(
                    process_func,
                    item,
                    exceptions=exceptions
                )
                results.append(result)
                
            except Exception as e:
                logger.error(f"Processtext {idx + 1} textFailed: {str(e)}")
                failures.append({
                    "index": idx,
                    "item": item,
                    "error": str(e)
                })
                
                if not continue_on_failure:
                    raise
        
        return results, failures

